// Self-host the site's three webfonts.
//
// Fetches the Google Fonts CSS2 stylesheet with a current-Chrome User-Agent
// (so Google serves woff2, not ttf), downloads every Latin woff2 it
// references into public/fonts/, and writes a rewritten stylesheet at
// public/fonts/webfonts.css that points at the local copies.
//
// Why self-host: the render-blocking third-party <link> to fonts.googleapis
// .com delayed first paint and sent every visitor's IP to Google — a Loi 25
// snag on a Quebec site. Same-origin woff2 + font-display:swap removes both.
//
// Latin-only: the site is FR/EN, so cyrillic/greek/vietnamese subsets are
// dropped — the browser would never request them anyway.
//
// Re-run when the font set changes: node scripts/fetch-webfonts.mjs

import { writeFileSync, mkdirSync, readdirSync, rmSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

// The site's font set, requested as VARIABLE fonts (weight ranges, not
// discrete weights) so Google returns one file per family/subset covering
// every weight, instead of one file per weight.
const CSS_URL =
  'https://fonts.googleapis.com/css2?family=Libre+Franklin:wght@400..800' +
  '&family=Source+Serif+4:ital,opsz,wght@0,8..60,400..700;1,8..60,400..700' +
  '&family=JetBrains+Mono:wght@400..600&display=swap'

// A current-Chrome UA makes Google Fonts return woff2 (older UAs get ttf).
const UA =
  'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 ' +
  '(KHTML, like Gecko) Chrome/124.0.0.0 Safari/537.36'

const KEEP_SUBSETS = new Set(['latin', 'latin-ext'])

const portalRoot = dirname(dirname(fileURLToPath(import.meta.url)))
const fontsDir = join(portalRoot, 'public', 'fonts')
mkdirSync(fontsDir, { recursive: true })

// Drop any previously-generated woff2 so a re-run can't leave orphans.
// The OG renderer's FiraSans-*.ttf files are a different format and stay.
for (const f of readdirSync(fontsDir)) {
  if (f.endsWith('.woff2')) rmSync(join(fontsDir, f))
}

const cssRes = await fetch(CSS_URL, { headers: { 'User-Agent': UA } })
if (!cssRes.ok) {
  console.error(`fetch-webfonts: CSS request failed — HTTP ${cssRes.status}`)
  process.exit(1)
}
const css = await cssRes.text()

// Google's CSS is a sequence of `/* subset */` comments each followed by one
// @font-face block. Keep only the Latin subsets.
const blockRe = /\/\*\s*([\w-]+)\s*\*\/\s*(@font-face\s*\{[^}]*\})/g
const counts = new Map()
const kept = []
let failed = 0

for (const match of css.matchAll(blockRe)) {
  const subset = match[1]
  let block = match[2]
  if (!KEEP_SUBSETS.has(subset)) continue

  const urlMatch = block.match(/url\((https:\/\/fonts\.gstatic\.com\/[^)]+\.woff2)\)/)
  if (!urlMatch) continue
  const remoteUrl = urlMatch[1]

  const slug = (remoteUrl.match(/\/s\/([a-z0-9]+)\//i)?.[1] ?? 'font').toLowerCase()
  const n = counts.get(slug) ?? 0
  counts.set(slug, n + 1)
  const localName = `${slug}-${n}.woff2`

  try {
    const res = await fetch(remoteUrl)
    if (!res.ok) throw new Error(`HTTP ${res.status}`)
    const buf = Buffer.from(await res.arrayBuffer())
    writeFileSync(join(fontsDir, localName), buf)
    console.log(`fetch-webfonts: ${localName} (${subset}, ${(buf.length / 1024).toFixed(1)} KB)`)
  } catch (err) {
    console.error(`fetch-webfonts: ${remoteUrl} failed — ${err.message}`)
    failed++
    continue
  }

  block = block.replace(remoteUrl, `/fonts/${localName}`)
  kept.push(`/* ${subset} */\n${block}`)
}

if (failed > 0 || kept.length === 0) {
  console.error(`fetch-webfonts: ${failed} download(s) failed, ${kept.length} kept — aborting.`)
  process.exit(1)
}

const header =
  '/* Self-hosted webfonts — generated by scripts/fetch-webfonts.mjs.\n' +
  '   Do not edit by hand; re-run the script to regenerate. */\n'
writeFileSync(join(fontsDir, 'webfonts.css'), header + kept.join('\n') + '\n')
console.log(`fetch-webfonts: ${kept.length} face(s) → public/fonts/webfonts.css`)
